class variable{
    public static void main(String args[]){
        byte b=5;
        System.out.println(b);
        b=25;
        System.out.println(b);
        byte c=30;
        System.out.println(c);
        float f=25.3f;
        System.out.println(f);
        double d=23.1;
        System.out.println(d);
        int x=5;
        System.out.println(Integer.toBinaryString(x));
        
        System.out.println(Integer.toHexString(x));

        
    }
}